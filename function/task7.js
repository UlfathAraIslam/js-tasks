function willSuccess(marks) {
  if (!Array.isArray(marks)) {
    return "Invalid";
  }
  let passCount = 0;
  let failCount = 0;

  for (const mark of marks) {
    if (typeof mark !== "number"){
        return "Invalid"
    } 
    mark >= 50 ? passCount++ : failCount++;
  }
  return passCount > failCount;
}


const marks = 90;

const result = willSuccess(marks);
